number_one = 10
number_two = 4
print("Numero 1: ",number_one,"  Numero 2: ",number_two)
result = number_one + number_two
print(number_one," + ",number_two,"    Suma:", result)
result = number_one - number_two
print(number_one," - ",number_two,"    Resta: ", result)
result = number_one * number_two
print(number_one," * ",number_two,"    Multiplicación: ", result)
result = number_one / number_two 	#Regresa un número flotante
print(number_one," / ",number_two,"    División: ", result)
result = number_one // number_two 	#Regresa un número entero
print(number_one," // ",number_two,"    División Entero: ", result)
result = number_one ** number_two 	#Regresa un número a su exponencial
print(number_one," ** ",number_two,"    Exponencial: ", result)
