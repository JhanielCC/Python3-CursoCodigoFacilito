"""
Tuplas
Las tuplas son un tipo de datos igual que las listas nos permiten almacenar diferentes tipos de datos, 
cualquier objeto, las tuplas son inmutables; una vez creada no puede ser modificada ni agregar ni quitar elementos. 
() tuplas, [] listas.
"""
print("<<<<<  TUPLAS  >>>>>")
my_tuple= (1,"string",True,"Python",3.1416,False)
print("Tupla  :",my_tuple)
print("Tupla posición[0]  :",my_tuple[0]) #Imprimir una posicion
print("Tupla posición[0] a [3]:",my_tuple[0:3]) #Imprimir una posicion y termina en la posicion

#Son buenas siempre que necesitemos un control total sobre diferentes tipos de datos y que estos sean constantes